//! Library data management — symbol filtering, SPICE preview generation, vendor model import.

use crate::app::NekoSpiceApp;
use osl_kicad::KicadIndexedSymbol;

#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) struct LibraryFilter {
    pub(crate) name: String,
    pub(crate) count: usize,
}

impl NekoSpiceApp {
    /// library filters。
    pub(crate) fn library_filters(&self) -> Vec<LibraryFilter> {
        let Some(library) = &self.library else {
            return vec![LibraryFilter {
                name: "All".to_string(),
                count: 0,
            }];
        };
        let mut filters = vec![LibraryFilter {
            name: "All".to_string(),
            count: library.index().symbols.len(),
        }];
        filters.extend(
            library
                .index()
                .libraries
                .iter()
                .map(|library| LibraryFilter {
                    name: library.name.clone(),
                    count: library.symbol_count,
                }),
        );
        filters
    }

    /// filtered library symbols。
    pub(crate) fn filtered_library_symbols(&self) -> Vec<KicadIndexedSymbol> {
        self.library
            .as_ref()
            .map(|library| library.filtered_index(&self.symbol_search).symbols)
            .unwrap_or_default()
    }

    /// selected library symbol snapshot。
    pub(crate) fn selected_library_symbol_snapshot(&self) -> Option<KicadIndexedSymbol> {
        let symbol_id = self.selected_symbol_id.as_deref()?;
        self.library
            .as_ref()
            .and_then(|library| library.symbol(symbol_id))
            .cloned()
    }
}

/// spice preview lines。
pub(crate) fn spice_preview_lines(symbol: &KicadIndexedSymbol) -> Vec<String> {
    let pin_numbers = symbol
        .pins
        .iter()
        .map(|pin| pin.number.as_str())
        .collect::<Vec<_>>();
    let pins = if pin_numbers.is_empty() {
        "1 2".to_string()
    } else {
        pin_numbers.join(" ")
    };
    vec![
        format!("* {}", symbol.id),
        format!(
            "* {}",
            symbol.description.as_deref().unwrap_or(&symbol.name)
        ),
        format!(".subckt {} {}", symbol.name, pins),
        "* KiCad symbol preview generated by NekoSpice".to_string(),
        "* Replace this stub with the vendor SPICE model when available.".to_string(),
        format!("* Source: {}", symbol.source),
        format!(".ends {}", symbol.name),
    ]
}

/// 厂商模型管理方法
impl NekoSpiceApp {
    /// 导入厂商 SPICE 模型目录
    pub(crate) fn import_vendor_models(&mut self, path: &str) {
        let path = std::path::PathBuf::from(path);
        if !path.is_dir() {
            self.status_message = Some(format!("Not a directory: {}", path.display()));
            return;
        }
        match osl_model::import_spice_model_dir(&path) {
            Ok(results) => {
                let catalog = osl_model::build_model_catalog(&results);
                let total = catalog.total_count();
                let subckt_count = catalog.subckts.len();
                let model_count = catalog.models.len();
                self.vendor_catalog = catalog;
                self.status_message = Some(format!(
                    "Loaded {} vendor models ({} subcircuits, {} .model)",
                    total, subckt_count, model_count
                ));
            }
            Err(err) => {
                self.status_message = Some(format!("Vendor import error: {err}"));
            }
        }
    }

    /// 导入单个厂商模型文件
    #[allow(dead_code)]
    pub(crate) fn import_vendor_model_file(&mut self, path: &str) {
        let path = std::path::PathBuf::from(path);
        if !path.is_file() {
            self.status_message = Some(format!("Not a file: {}", path.display()));
            return;
        }
        match osl_model::import_spice_model_file(&path) {
            Ok(result) => {
                let subckt_count = result.subckts.len();
                let model_count = result.models.len();
                // Merge into existing catalog
                for subckt in &result.subckts {
                    self.vendor_catalog.subckts.insert(
                        subckt.name.clone(),
                        osl_model::ModelCatalogEntry {
                            name: subckt.name.clone(),
                            pins: subckt.pins.clone(),
                            source: subckt.source_file.display().to_string(),
                            vendor: subckt.vendor,
                            body: subckt.body.clone(),
                        },
                    );
                }
                for model in &result.models {
                    self.vendor_catalog.models.insert(
                        model.name.clone(),
                        osl_model::ModelCatalogEntry {
                            name: model.name.clone(),
                            pins: Vec::new(),
                            source: model.source_file.display().to_string(),
                            vendor: model.vendor,
                            body: format!(".model {} {}", model.name, model.params),
                        },
                    );
                }
                self.status_message = Some(format!(
                    "Imported {} subcircuits, {} models from {}",
                    subckt_count, model_count, path.display()
                ));
            }
            Err(err) => {
                self.status_message = Some(format!("Vendor import error: {err}"));
            }
        }
    }

    /// 搜索厂商模型
    #[allow(dead_code)]
    pub(crate) fn filtered_vendor_models(&self) -> &osl_model::VendorModelCatalog {
        if self.vendor_search.trim().is_empty() {
            &self.vendor_catalog
        } else {
            // We need to return a reference, but search creates a new catalog
            // For now, just return the full catalog
            &self.vendor_catalog
        }
    }
}
